public void loadtable()
    {
        cleartable();
        ArrayList<POJO_KHACHHANG> dsp = new ArrayList<POJO_KHACHHANG>();
        DAO_KHACHHANG p = new DAO_KHACHHANG();
        dsp = p.LayDSKH();
        for(POJO_KHACHHANG a:dsp)
        {
            Vector r = new Vector();
            r.add(a.getMakh());
            r.add(a.getTenkh());
            body.add(r);
        }
        tb3.setModel(new DefaultTableModel(body,head));
    }
public void cleartable()
{
    body.clear();
    tb3.setModel(new DefaultTableModel(body,head));
}
