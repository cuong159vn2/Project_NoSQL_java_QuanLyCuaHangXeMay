    private void tb3MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb3MousePressed
    try {
        int dong = tb3.getSelectedRow();
        POJO_KHACHHANG a = new POJO_KHACHHANG();
        DAO_KHACHHANG p = new DAO_KHACHHANG();
        a=p.inforeKhachHang(tb3.getValueAt(dong, 0).toString());
        txt_ma.setText(a.getMakh());
        txt_ten.setText(a.getTenkh());
        txt_diachi.setText(a.getDiachi());
        txt_dienthoai.setText(a.getSdt());
    } catch (ParseException ex) {
        Logger.getLogger(KhachHang.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_tb3MousePressed

