    private void tb1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb1MousePressed
     try {
         int dong = tb1.getSelectedRow();
         POJO_NHANVIEN b = new POJO_NHANVIEN();
         DAO_NhanVien p = new DAO_NhanVien();
         b=p.inforeNhanVien(tb1.getValueAt(dong, 1).toString());
         txt_cv.setText(b.getChucvu());
         txt_dc.setText(b.getDiachinv());
         txt_gt.setText(b.getGioitinh());
         txt_luong.setText(b.getLuong());
         txt_mach.setText(b.getMach());
         txt_manv.setText(b.getManv());
         txt_manv.setText(b.getManv());
         txt_mk.setText(b.getMatkhau());
         txt_ten.setText(b.getTennv());
         txt_tk.setText(b.getTaikhoan());
     } catch (ParseException ex) {
         Logger.getLogger(NhanVien.class.getName()).log(Level.SEVERE, null, ex);
     }
    }//GEN-LAST:event_tb1MousePressed

