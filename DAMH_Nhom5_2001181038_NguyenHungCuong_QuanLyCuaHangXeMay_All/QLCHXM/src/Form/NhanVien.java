public void loadtable()
    {
        cleartable();
        ArrayList<POJO_NHANVIEN> dsp = new ArrayList<POJO_NHANVIEN>();
        DAO_NhanVien p = new DAO_NhanVien();
        dsp = p.LayDSNV();
        for(POJO_NHANVIEN a:dsp)
        {
            Vector r = new Vector();
            r.add(a.getMach());
            r.add(a.getManv());
            body.add(r);
        }
        tb1.setModel(new DefaultTableModel(body,head));
    }
