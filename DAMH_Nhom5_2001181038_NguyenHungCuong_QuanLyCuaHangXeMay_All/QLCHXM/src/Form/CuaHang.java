    private void tb1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tb1MousePressed
        int dong = tb1.getSelectedRow();
        POJO_CUAHANG a = new POJO_CUAHANG();
        DAO_CUAHANG p = new DAO_CUAHANG();
    try {
        a=p.inforeCuaHang2(tb1.getValueAt(dong, 0).toString());
    } catch (ParseException ex) {
        Logger.getLogger(CuaHang.class.getName()).log(Level.SEVERE, null, ex);
    }
        txt_ma.setText(a.getMach());
        txt_ten.setText(a.getTench());
        txt_diachi.setText(a.getDiaChi());
    }//GEN-LAST:event_tb1MousePressed

