public void loadtable()
    {
        cleartable();
        ArrayList<POJO_CUAHANG> dsp = new ArrayList<POJO_CUAHANG>();
        DAO_CUAHANG p = new DAO_CUAHANG();
        dsp = p.LayDS();
        for(POJO_CUAHANG a:dsp)
        {
            Vector r = new Vector();
            r.add(a.getMach());
            r.add(a.getTench());
            body.add(r);
        }
        tb1.setModel(new DefaultTableModel(body,head));
    }
public void cleartable()
{
    body.clear();
    tb1.setModel(new DefaultTableModel(body,head));
}
